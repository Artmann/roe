#[test]
fn kill_list_keeps_every_false_positive_candidate() {
    // The would-be false positives that must NOT appear:
    // - PoliteGreeter.Greet (interface-only dispatch)
    // - Circle.Render (override-only usage)
    // - SqlRepo/Worker (DI generic registration), Worker.StartAsync/StopAsync
    //   (external IHostedService heuristic)
    // - UserDto + auto-properties (serializer binding)
    // - StringExtensions.Shout (extension method, ungated)
    // - Order.Total (nameof), Migration001 (typeof)
    // - Program.OnClicked (event handler +=)
    // - MathHelper.Square (using static)
    // - SecuredThing ([Authorize] attribute-rooted), AuthorizeAttribute
    //   (attribute name shorthand)
    // - Status.Legacy (enum member, default mode)
    // - Pair.Deconstruct (invoked implicitly by `var (a, b) = ...`)
    //
    // True positives that MUST appear:
    // - DeadService (type gate: live.Process() elsewhere must not save it)
    // - ClusterHost.unusedField + DeadHelperMethod (dead cluster)
    assert_findings(
        findings("kill_list", false),
        vec![
            ty("Kill.DeadService"),
            member("Kill.ClusterHost.unusedField"),
            member("Kill.ClusterHost.DeadHelperMethod"),
        ],
    );
}

#[test]
fn kill_list_aggressive_flags_enum_members_and_auto_properties() {
    let actual = findings("kill_list", true);
    // Aggressive adds the serialization-prone members that default mode
    // exempts; Order.Total survives because nameof() references it and
    // Status.Active because it is named in Program.
    let must_appear = [
        member("Kill.Status.Legacy"),
        member("Kill.UserDto.Name"),
        member("Kill.UserDto.UnreadCount"),
    ];
    for expected in &must_appear {
        assert!(
            actual.contains(expected),
            "missing {expected:?} in {actual:?}"
        );
    }
    let must_not_appear = [member("Kill.Order.Total"), member("Kill.Status.Active")];
    for unexpected in &must_not_appear {
        assert!(!actual.contains(unexpected), "unexpected {unexpected:?}");
    }
}

#[test]
fn manual_roots_rescue_symbols() {
    let analysis = analyze(
        &fixture("console_app"),
        false,
        &["ConsoleApp.ConsoleGreeter.UnusedHelper".to_string()],
    )
    .expect("analysis should succeed");
    let names: Vec<&str> = analysis
        .result
        .findings
        .iter()
        .map(|f| f.name.as_str())
        .collect();
    assert!(!names.contains(&"ConsoleApp.ConsoleGreeter.UnusedHelper"));
}

