use std::path::PathBuf;

use roe::commands::dead_code::analyze;
use roe::model::FindingKind;

fn fixture(name: &str) -> PathBuf {
    PathBuf::from(env!("CARGO_MANIFEST_DIR"))
        .join("tests/fixtures")
        .join(name)
}

/// Sorted (kind, name) pairs for stable assertions.
fn findings(name: &str, aggressive: bool) -> Vec<(FindingKind, String)> {
    let analysis = analyze(&fixture(name), aggressive, &[]).expect("analysis should succeed");
    let mut pairs: Vec<(FindingKind, String)> = analysis
        .result
        .findings
        .iter()
        .map(|f| (f.kind, f.name.clone()))
        .collect();
    pairs.sort_by(|a, b| a.1.cmp(&b.1));
    pairs
}

fn file(kind_name: &str) -> (FindingKind, String) {
    (FindingKind::UnusedFile, kind_name.to_string())
}

fn ty(kind_name: &str) -> (FindingKind, String) {
    (FindingKind::UnusedType, kind_name.to_string())
}

fn member(kind_name: &str) -> (FindingKind, String) {
    (FindingKind::UnusedMember, kind_name.to_string())
}

fn assert_findings(actual: Vec<(FindingKind, String)>, mut expected: Vec<(FindingKind, String)>) {
    expected.sort_by(|a, b| a.1.cmp(&b.1));
    assert_eq!(actual, expected);
}

#[test]
fn console_app_finds_dead_class_and_member() {
    assert_findings(
        findings("console_app", false),
        vec![
            file("ConsoleApp/DeadClass.cs"),
            member("ConsoleApp.ConsoleGreeter.UnusedHelper"),
        ],
    );
}

